a = 56
b = 10

# a & b
print("a & b =", a & b)  

# a | b
print("a | b =", a | b)  

# a ^ b
print("a ^ b =", a ^ b)  

# Left shift both a and b with 2 bits
a = a << 2
b = b << 2
print("Left shifted a =", a)
print("Left shifted b =", b)  

# Right shift a with 2 bits and b with 4 bits
a = a >> 2
b = b >> 4
print("Right shifted a =", a)
print("Right shifted b =", b)  
